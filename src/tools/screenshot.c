static void draw_char(gui_dbuf_t *db, int x, int y, char c, uint32_t color) {
    const uint8_t *g = font8[(unsigned char)c];
    for (int r = 0; r < 8; r++)
        for (int b = 0; b < 8; b++)
            if (g[r] & (1 << (7 - b)))
                gui_dbuf_pixel(db, x + b, y + r, color);
}

static void draw_text(gui_dbuf_t *db, int x, int y, const char *s, uint32_t color) {
    while (*s) { if (*s != ' ') draw_char(db, x, y, *s, color); x += 8; s++; }
}

static void save_ppm(const char *path, uint32_t *fb, int w, int h) {
    FILE *f = fopen(path, "wb");
    if (!f) return;
    fprintf(f, "P6\n%d %d\n255\n", w, h);
    for (int i = 0; i < w * h; i++) {
        uint32_t c = fb[i];
        putc((c >> 16) & 0xFF, f);
        putc((c >> 8) & 0xFF, f);
        putc(c & 0xFF, f);
    }
    fclose(f);
    printf("Saved: %s (%dx%d)\n", path, w, h);
}

